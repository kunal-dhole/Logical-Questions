package com.logical;

public class ZeroPositiveOrNegative {

	public static void main(String[] args) {
		
		int n=-10;
		
		if(n==0) {
			System.out.println(n+ " is zero");
		}else {

			if(n>0) {
				System.out.println(n +" is positive");
			}else {
				System.out.println(n +" is negative");
			}
			
		}
		
		
	}

}
